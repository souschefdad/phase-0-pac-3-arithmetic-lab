function add(a, b) {
    return a + b;
}

function subtract(a, b) {
    return a - b;
}

function multiply(a, b) {
    return a * b;
}

function divide(a, b) {
    return a / b;
}

function increment(n) {
    return ++n;
}

function decrement(n) {
    return --n;
}

function makeInt(n) {
    let parInt = parseInt(n, 10);
    return parInt;
}

function preserveDecimal(n) {
    let preDec = parseFloat(n);
    return preDec;
}